            <h4>Morning Briefing</h4>
            <p>Morning briefing dilakukan setiap hari Seni s/d Jumat pada pukul 10.10-10.30 WIB dan diikuti oleh para
                guru, staf, dan laboran. Dalam kegiatan tersebut setiap orang diberikan kesempatan untuk memberikan
                gagasan dan suatu informasi, misalnya pemberian kalimat motivasi, bercerita tentang informasi tertentu
                yang didapatkan dari internet/media/kejadian langsung, tips & trik, seni, atau senam kecil yang sifatnya
                menyegarkan otak dan tubuh. Tujuan dilakukannya briefing adalah untuk memberikan semangat kerja yang
                baik sehingga diperoleh manfaat agar seluruh bagian mendapat perencanaan dan pembelajaran seperti apa
                yang akan dilakukan hari ini, juga diharapkan tumbuhnya rasa kerja sama yang baik antar satu sama lain.
            </p>
