                memberikan gambaran tentang dunia SMK yang berbeda dengan jenjang sekolah kesetaraannya.Tahun 2017 lalu
                SMK Wikrama membuka jurusan Jasa Boga dan Akomodasi Perhotelan, hal tersebut memberikan warna baru yang
                diharapkan makin memerkuat karakter Wikrama dimasa yang akan datang. Kegiatan In House Training ini
                selain dimaksudkan untuk menumbuhkan rasa persaudaraan antar warga di SMK Wikrama Bogor, juga sebagai
                akses untuk mengetahui hal-hal baru yang mungkin saja tidak diterapkan di sekolah lain. Rayonisasi,
