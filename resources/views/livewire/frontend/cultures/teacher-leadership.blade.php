            <h4>Academic Group Meeting</h4>
            <p>Setiap satu bulan sekali SMK Wikrama mengadakan Academic Group Meeting yang berkisar pada tanggal 10 atau
                11, kegiatan tersebut tentunya melibatkan semua guru, asisten guru, dan laboran. Pembahasan seputar
                pengembangan kurikulum, pengembangan kompetensi guru, desiminasi (sharing) hasil pelatihan dan micro
                teaching. Manfaat tentang beberapa aspek bisa lebih meningkat (pedagogik, kepribadian, sosial dan
                profesional), meningkatkan keharmonisan antar guru, menambah pengetahuan baru (media yang diberikan bisa
