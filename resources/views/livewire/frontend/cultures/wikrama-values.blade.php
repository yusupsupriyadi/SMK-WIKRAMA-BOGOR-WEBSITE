            <p>Bersih untuk diri sendiri, bersikap jujur merupakan realisasi dari wujud kebersihan untuk diri sendiri.
                Ketika melihat lingkungan sekitar kotor, siswa/i SMK Wikrama diharapkan untuk saling bekerja sama
                membersihkan lingkungan sekitar, bukan justru saling menyuruh, melainkan menggandeng yang lain untuk
                senantiasa mencintai kebersihan. Kebersihan hati juga termasuk didalamnya, sifat-sifat negatif yang
                membuat seorang insan membuahkan keburukan-keburukan untuk dirinya sendiri dan orang lain sebaiknya
                dijauhkan. SMK Wikrama sangat mengedepankan hal-hal yang wajib, ibadah sunnah pun tak lupa diutamakan.
                Shalat 5 waktu, sedekah jariyah, shalat-shalat sunnah, pun banyak hal lainnya yang membuat kereligian
                setiap siswa/i semakin kuat seiring bertambahnya hari. Seperti halnya jujur, bersih pun harus dijadikan
