            <h2>Berjamaah</h2>
            <p>Mendengar kata “berjemaah” mindset kita langsung mengarah pada shalat berjamaah. Hal tersebut tidak salah
                karena shalat berjamaah di SMK Wikrama merupakan “kebutuhan” wajib yang seyogyanya diterapkan oleh
                siswa/i muslim di sekolah ini. Begitupun dalam hal apapun, berjemaah haruslah menjadi bagian dari
                kebiasaan di SMK Wikrama. Berjemaah dalam membersihkan lingkungan, berjemaah dalam mengingatkan hal-hal
                yang salah, juga termasuk berjemaah dalam memberikan senyum. Kalau berjemaah dalam mengerjakan ulangan
                boleh juga, dong? Untuk satu ini, perlu digarisbawahi, jika berjemaah dalam mempersiapkan diri
                menghadapi ulangan—dalam arti bekerja kelompok untuk memecahkan soal-soal yang rumit bin ajaib—boleh
                saja, namun jika berjemaah dalam mencontek itu yang tidak baik, seperti pepatah yang mengatakan,
                “Berbondong-bondonglah kamu dalam melakukan kebaikan, bukan justru sebaliknya.”</p>
